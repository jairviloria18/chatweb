const userModel = require("../models/userModel");
const bcryptjs = require("bcryptjs");
const validator = require("validator");
const jwt = require("jsonwebtoken");

const createToken = (_id) => {
  const jwtkey = process.env.JWT_SECRET_KEY;

  return jwt.sign({ _id }, jwtkey, { expiresIn: "3d" });
};

//USERS REGISTER
const registerUser = async (req, res) => {
  try {
    const { name, email, password } = req.body;

    let user = await userModel.findOne({ email });

    if (user)
      return res.status(400).json("Ya existe un usuario con ese email.");

    if (!name || !email || !password)
      return res.status(400).json("Todos los campos son requeridos.");

    if (!validator.isEmail(email))
      return res.status(400).json("El email debe ser un email valido.");

    if (!validator.isStrongPassword(password))
      return res.status(400).json("El password debe ser un password valido.");

    user = new userModel({ name, email, password });

    const salt = await bcryptjs.genSalt(10);
    user.password = await bcryptjs.hash(user.password, salt);

    await user.save();

    const token = createToken(user._id);

    res.status(200).json({ _id: user._id, name, email, token });
  } catch (error) {
    console.log(error);
    res.status(500).json(error);
  }
};

//LOGIN
const loginUser = async (req, res) => {
  const { email, password } = req.body;

  try {
    let user = await userModel.findOne({ email });

    if (!user) return res.status(400).json("Email or password incorrectos.");

    const isValidPassword = await bcryptjs.compare(password, user.password);

    if (!isValidPassword)
      return res.status(400).json("Email or password incorrectos.");

    const token = createToken(user._id);

    res.status(200).json({ _id: user._id, name: user.name, email, token });
  } catch (error) {
    console.log(error);
    res.status(500).json(error);
  }
};

//FINDUSER
const findUser = async (req, res) => {
  const userId = req.params.userId;

  try {
    const user = await userModel.findById(userId);

    res.status(200).json(user);
  } catch (error) {
    console.log(error);
    res.status(500).json(error);
  }
};

//GETUSERS
const getUsers = async (req, res) => {
  //const userId = req.params.userId;

  try {
    const users = await userModel.find();

    res.status(200).json(users);
  } catch (error) {
    console.log(error);
    res.status(500).json(error);
  }
};

module.exports = { registerUser, loginUser, findUser, getUsers };
